package PracticeInterview.LeetCode;

public class TreeNodeTemp {
    int val;
    TreeNodeTemp left;
    TreeNodeTemp right;

    TreeNodeTemp() {
    }

    TreeNodeTemp(int val) {
        this.val = val;
    }

    TreeNodeTemp(int val, TreeNodeTemp left, TreeNodeTemp right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
